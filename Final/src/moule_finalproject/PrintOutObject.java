package moule_finalproject;

public class PrintOutObject{

	private String Book_Title;
	private String Book_Code;
	private double Book_Price;
	private String Author_Num;
	private String Author_FullName;
	private String Publisher_Code;
	private String Publisher_Name;
	
	public String getBook_Code() {
		return Book_Code;
	}//getBook_Code

	public void setBook_Code(String book_Code) {
		Book_Code = book_Code;
	}//setBook_Code

	public double getBook_Price() {
		return Book_Price;
	}

	public void setBook_Price(double book_Price) {
		Book_Price = book_Price;
	}//setBook_Price

	public String getAuthor_Num() {
		return Author_Num;
	}//getAuthor_Num

	public void setAuthor_Num(String author_Num) {
		Author_Num = author_Num;
	}//setAuthor_Num

	public String getAuthor_FullName() {
		return Author_FullName;
	}//getAuthor_FullName

	public void setAuthor_FullName(String author_FullName) {
		Author_FullName = author_FullName;
	}//setAuthor_FullName

	public String getPublisher_Code() {
		return Publisher_Code;
	}//getPublisher_Code

	public void setPublisher_Code(String publisher_Code) {
		Publisher_Code = publisher_Code;
	}//setPublisher_Code

	public String getPublisher_Name() {
		return Publisher_Name;
	}//getPublisher_Name

	public void setPublisher_Name(String publisher_Name) {
		Publisher_Name = publisher_Name;
	}//setPublisher_Name
	

	public String getBook_Title() {
		return Book_Title;
	}//setBook_Code
	

	public void setBook_Title(String book_Title) {
		Book_Title = book_Title;
	}//setBook_Title
}//PrintOutObject